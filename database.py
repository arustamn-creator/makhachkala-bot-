import sqlite3
import os

DB_PATH = os.environ.get("DB_PATH", "makhachkala.db")
_db_dir = os.path.dirname(DB_PATH)
if _db_dir:
    os.makedirs(_db_dir, exist_ok=True)


def get_db():
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db():
    conn = get_db()
    c = conn.cursor()

    c.executescript("""
    CREATE TABLE IF NOT EXISTS users (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        tg_id INTEGER UNIQUE,
        username TEXT,
        first_name TEXT,
        last_name TEXT,
        phone TEXT,
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE IF NOT EXISTS masters (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        spec TEXT NOT NULL,
        category TEXT NOT NULL,
        emoji TEXT DEFAULT '👨‍🔧',
        bg TEXT DEFAULT 'rgba(59,130,246,0.12)',
        rating REAL DEFAULT 5.0,
        reviews INTEGER DEFAULT 0,
        price TEXT,
        price_unit TEXT,
        experience TEXT,
        phone TEXT,
        online INTEGER DEFAULT 0,
        skills TEXT,
        description TEXT,
        plan TEXT DEFAULT 'basic',
        active INTEGER DEFAULT 1,
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE IF NOT EXISTS shops (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT NOT NULL,
        category TEXT NOT NULL,
        emoji TEXT DEFAULT '🏪',
        bg TEXT DEFAULT 'rgba(200,169,81,0.12)',
        description TEXT,
        address TEXT,
        phone TEXT,
        hours TEXT,
        products TEXT,
        plan TEXT DEFAULT 'basic',
        active INTEGER DEFAULT 1,
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE IF NOT EXISTS requests (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_tg_id INTEGER,
        user_name TEXT,
        user_phone TEXT,
        description TEXT,
        master_id INTEGER,
        shop_id INTEGER,
        request_type TEXT DEFAULT 'master',
        status TEXT DEFAULT 'new',
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );

    CREATE TABLE IF NOT EXISTS products (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        shop_id INTEGER NOT NULL REFERENCES shops(id),
        emoji TEXT DEFAULT '📦',
        name TEXT NOT NULL,
        price INTEGER NOT NULL,
        unit TEXT DEFAULT 'шт',
        active INTEGER DEFAULT 1
    );

    CREATE TABLE IF NOT EXISTS cart_items (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_key TEXT NOT NULL,
        product_id INTEGER NOT NULL REFERENCES products(id),
        quantity INTEGER DEFAULT 1,
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
        UNIQUE(user_key, product_id)
    );

    CREATE TABLE IF NOT EXISTS orders (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        user_key TEXT NOT NULL,
        customer_name TEXT,
        customer_phone TEXT,
        customer_address TEXT,
        shop_id INTEGER REFERENCES shops(id),
        items_json TEXT NOT NULL,
        total INTEGER NOT NULL,
        status TEXT DEFAULT 'new',
        notified INTEGER DEFAULT 0,
        created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    );
    """)

    _add_column_if_missing(c, "shops", "owner_code", "TEXT")
    _add_column_if_missing(c, "shops", "owner_tg_id", "INTEGER")
    _add_column_if_missing(c, "masters", "owner_code", "TEXT")
    _add_column_if_missing(c, "masters", "owner_tg_id", "INTEGER")
    c.execute("CREATE UNIQUE INDEX IF NOT EXISTS idx_products_shop_name ON products(shop_id, name)")

    conn.commit()
    conn.close()


def _add_column_if_missing(cursor, table, column, coltype):
    cols = [r[1] for r in cursor.execute(f"PRAGMA table_info({table})").fetchall()]
    if column not in cols:
        cursor.execute(f"ALTER TABLE {table} ADD COLUMN {column} {coltype}")


